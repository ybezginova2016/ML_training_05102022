# (–Ω–æ–∂–∫–∏ –∏ –∫—Ä–µ–ø–ª–µ–Ω–∏—è) –∏ –≤–∏–Ω—Ç—ã. –ß—Ç–æ–±—ã –ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ –æ–¥–∏–Ω —Å—Ç—É–ª, —Å–∫–∞–º—å—é
# –∏ —Å—Ç–æ–ª, —Ç—Ä–µ–±—É–µ—Ç—Å—è –º–∞—Ç–µ—Ä–∏–∞–ª–æ–≤ –≤ —Ç–∞–∫–æ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–µ.

# –í—ã—á–∏—Å–ª–∏—Ç–µ, —Å–∫–æ–ª—å–∫–æ –ø–æ–Ω–∞–¥–æ–±–∏—Ç—Å—è –º–∞—Ç–µ—Ä–∏–∞–ª–æ–≤, —á—Ç–æ–±—ã –ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ 16 —Å—Ç–æ–ª–æ–≤,
# 60 —Å—Ç—É–ª—å–µ–≤ –∏ 4 —Å–∫–∞–º—å–∏. –°–æ—Ö—Ä–∞–Ω–∏—Ç–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π materials
# –∏ –Ω–∞–ø–µ—á–∞—Ç–∞–π—Ç–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –Ω–∞ —ç–∫—Ä–∞–Ω–µ.

# –í –º–∞—Ç—Ä–∏—Ü–µ manufacture –∫–∞–∂–¥–∞—è —Å—Ç—Ä–æ–∫–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ø—Ä–µ–¥–º–µ—Ç—É –º–µ–±–µ–ª–∏,
# –∞ —Å—Ç–æ–ª–±–µ—Ü ‚Äî –º–∞—Ç–µ—Ä–∏–∞–ª—É. –ß—Ç–æ–±—ã –≤—ã—á–∏—Å–ª–∏—Ç—å –Ω—É–∂–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥—Ä–µ–≤–µ—Å–Ω–æ–π
# –ø–ª–∏—Ç—ã, —Å–∫–∞–ª—è—Ä–Ω–æ —É–º–Ω–æ–∂—å—Ç–µ –Ω—É–ª–µ–≤–æ–π —Å—Ç–æ–ª–±–µ—Ü –Ω–∞ –≤–µ–∫—Ç–æ—Ä furniture.
# –ü–æ–ª–Ω—ã–π —Å–ø–∏—Å–æ–∫ –º–∞—Ç–µ—Ä–∏–∞–ª–æ–≤ ‚Äî —ç—Ç–æ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ —Ç—Ä–∞–Ω—Å–ø–æ–Ω–∏—Ä–æ–≤–∞–Ω–Ω–æ–π
# –º–∞—Ç—Ä–∏—Ü—ã manufacture.T –Ω–∞ –≤–µ–∫—Ç–æ—Ä furniture.

import numpy  as np
import pandas as pd

materials_names = ['–î—Ä–µ–≤–µ—Å–Ω–∞—è –ø–ª–∏—Ç–∞', '–ú–µ—Ç–∞–ª–ª–∏—á–µ—Å–∫–∞—è —Ç—Ä—É–±–∞', '–í–∏–Ω—Ç—ã']

# –∞–Ω–≥–ª. –ø—Ä–æ–∏–∑–≤–æ–¥—Å—Ç–≤–æ
manufacture = np.array([
    [0.2, 1.2, 8],
    [0.5, 0.8, 6],
    [0.8, 1.6, 8]])

# –∞–Ω–≥–ª. –º–µ–±–µ–ª—å
